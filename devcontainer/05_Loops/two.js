// while loops

let i = 1
while (i <= 10 ) {
    console.log(`value of i ${i}`);
    i = i + 2 
}

let myarray = ["flash", "batman", "superman"]
let arr = 0
while (arr < myarray.length) {
    console.log(`value of ${myarray[arr]}`);
    arr = arr+1
}

